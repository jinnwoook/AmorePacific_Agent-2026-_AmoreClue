async function addAllDescriptions() {
  const client = new MongoClient(process.env.MONGODB_URI || 'mongodb://localhost:27017');
  await client.connect();
  const db = client.db('amore');

  console.log('전체 키워드 설명 추가 시작...\n');

  let updatedCount = 0;
  let notFoundCount = 0;

  for (const [keyword, data] of Object.entries(keywordDescriptions)) {
    // 해당 키워드의 모든 문서 업데이트 (대소문자 구분 없이)
    const result = await db.collection('processed_keywords').updateMany(
      { keyword: { $regex: new RegExp(`^${keyword.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}$`, 'i') } },
      {
        $set: {
          description: data.description,
          koreanName: data.koreanName
        }
      }
    );

    if (result.modifiedCount > 0) {
      console.log(`✅ "${keyword}": ${result.modifiedCount}개 문서 업데이트`);
      updatedCount += result.modifiedCount;
    } else if (result.matchedCount === 0) {
      notFoundCount++;
    }
  }

  console.log(`\n===== 완료 =====`);
  console.log(`업데이트된 문서: ${updatedCount}개`);
  console.log(`DB에 없는 키워드: ${notFoundCount}개`);

  // 결과 확인
  const remaining = await db.collection('processed_keywords').aggregate([
    { $match: { $or: [{ description: null }, { description: '' }, { description: { $exists: false } }] } },
    { $group: { _id: '$keyword', avgScore: { $avg: '$score' } } },
    { $sort: { avgScore: -1 } },
    { $limit: 30 }
  ]).toArray();

  console.log(`\n아직 설명 없는 상위 키워드 ${remaining.length}개:`);
  remaining.forEach(k => console.log(` - ${k._id} (점수: ${Math.round(k.avgScore)})`));

  await client.close();
}

addAllDescriptions().catch(console.error);
